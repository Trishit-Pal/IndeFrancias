const DEVANAGARI = /[\u0900-\u097F]/;
const SCRIPT = {
	ta: /[\u0B80-\u0BFF]/,
	te: /[\u0C00-\u0C7F]/,
	kn: /[\u0C80-\u0CFF]/
};

for (const lang of ['ta', 'te', 'kn']) {
	for (const key of keys) {
		const val = T[lang]?.[key];
		if (!val) throw new Error(`Missing ${lang} translation for: ${key}`);
		if (DEVANAGARI.test(val)) throw new Error(`Devanagari in ${lang}: ${key}`);
		if (!SCRIPT[lang].test(val)) throw new Error(`Wrong script in ${lang}: ${key} => ${val}`);
	}
}

const out = { ta: {}, te: {}, kn: {} };
for (const key of keys) {
	out.ta[key] = T.ta[key];
	out.te[key] = T.te[key];
	out.kn[key] = T.kn[key];
}

writeFileSync(OUT, JSON.stringify(out, null, 2) + '\n', 'utf8');
